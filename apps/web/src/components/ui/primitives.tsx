/**
 * Radix wrappers.
 *
 * Radix supplies behaviour — focus trapping, keyboard navigation, ARIA wiring,
 * portalling — and nothing else. These add the styling. Hand-rolling a select
 * that is genuinely operable by keyboard and announced correctly by a screen
 * reader is a week of work most teams skip; this is why we do not skip it.
 */

